states_of_America = ["Illinois", "New Jersey", "New York", "Alabama", "Texas", "Atlanta"]

# If we want to make a change in the list, we can do it by following way
states_of_America[1] = "South Carolina"  # which changes New Jersey with South Carolina
print(states_of_America)

# If you want to add something to the list, you can just append like:
states_of_America.append("California")
print(states_of_America)

