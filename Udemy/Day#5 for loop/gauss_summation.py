# The Gauss Summation is named for Johann Karl Friedrich Gauss. 
# He was a German mathematician. Gauss is one of history’s most influential mathematical thinkers. A legend suggests that Gauss came up with a new method of summing sequences at a very young age. 
# The legend says that his math teacher asked the class to add the numbers 1 to 100. 
# In other words, the teacher wanted them to add 1 + 2 + 3 + 4 + 5… all the way up to 100!

sum = 0
for number in range(101):
    sum += number
print(sum)

